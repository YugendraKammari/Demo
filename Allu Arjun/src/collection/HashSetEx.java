package collection;

import java.util.HashSet;

public class HashSetEx {

	public static void main(String[] args) {
		HashSet<String> h=new HashSet<>();
		h.add("Hari");  //Program to demonstrate the HashSet
		h.add("Tharun");
		h.add("Bhargav");
		h.add("Hemanth");
		h.add("Satish");
		h.add("Tiger");
		System.out.println(h);
		System.out.println(h.contains("Tiger"));
		System.out.println(h.size());
		
				
	
	
	}

}
